/**
 * Shared emitters for TypeScript-targeting adapters: records, enums, unions, Zod schemas.
 *
 * Spec reference: openstrux-spec/specs/modules/target-nextjs/generator.md §4
 */

import type { TypeRecord, TypeEnum, TypeUnion, FieldAnnotation, TypeBlockAnnotation } from "@openstrux/ast";
import type { GeneratedFile } from "../../types.js";
import { tsType, prismaType, zodType, collectTypeRefs } from "./types.js";

const FILE_HEADER = "// generated by openstrux-generator — do not edit\n";

// ---------------------------------------------------------------------------
// Prisma annotation translation helpers
// ---------------------------------------------------------------------------

/** Translate a @pk annotation to Prisma field directives. */
function prismaFromPk(ann: Extract<FieldAnnotation, {kind: "pk"}>, fieldTypePrisma: string): string {
  const gen = ann.default
    ? ann.default
    : fieldTypePrisma === "Int" ? "autoincrement" : "cuid";
  return `@id @default(${gen}())`;
}

/** Translate a @default annotation to Prisma directive. */
function prismaFromDefault(ann: Extract<FieldAnnotation, {kind: "default"}>): string {
  const v = ann.value;
  if (v === "now") return "@default(now())";
  if (typeof v === "boolean") return `@default(${String(v)})`;
  if (typeof v === "number") return `@default(${String(v)})`;
  return `@default(${JSON.stringify(v)})`;
}

/** Translate a @relation annotation to Prisma directive. */
function prismaFromRelation(ann: Extract<FieldAnnotation, {kind: "relation"}>): string {
  const parts = [`fields: [${ann.field}]`, `references: [${ann.ref.field}]`];
  if (ann.onDelete) parts.push(`onDelete: ${ann.onDelete}`);
  if (ann.onUpdate) parts.push(`onUpdate: ${ann.onUpdate}`);
  return `@relation(${parts.join(", ")})`;
}

/** Returns Prisma field-level annotation strings for a field. */
function prismaFieldAnnotations(annotations: readonly FieldAnnotation[], fieldTypePrisma: string): string {
  const parts: string[] = [];
  for (const ann of annotations) {
    switch (ann.kind) {
      case "pk":        parts.push(prismaFromPk(ann, fieldTypePrisma)); break;
      case "default":   parts.push(prismaFromDefault(ann)); break;
      case "unique":    parts.push("@unique"); break;
      case "relation":  parts.push(prismaFromRelation(ann)); break;
      case "updatedAt": parts.push("@updatedAt"); break;
      case "column":    parts.push(`@map("${ann.name}")`); break;
      case "ignore":    parts.push("@ignore"); break;
    }
  }
  return parts.length > 0 ? " " + parts.join(" ") : "";
}

/** Simple heuristic plural for inverse relation field name: lowercase first char + 's'. */
function pluralFieldName(typeName: string): string {
  if (typeName.length === 0) return "items";
  return typeName.charAt(0).toLowerCase() + typeName.slice(1) + "s";
}

